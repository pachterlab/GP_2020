%10th order Padé approximation (Luke 1969, v2, p.107-108, table 4.2)
function e1 = e1_pade_n(z,n)
el=0.5772156649015328;
% p1 = [1,8.5733287401,18.0590169730,8.6347608925,0.2677737343];
% p2 = [1,9.5733223454,25.6329561486,21.0996530827,3.9584969228];
switch n
    case 0
        p1 = [1];
        p2 = [1];
    case 1
        p1 = [0,4];
        p2 = [1,4];
    case 2
        p1 = [0,3,36];
        p2 = [1,12,36];
    case 3
        p1 = [0,17/3,60,480];
        p2 = [1,24,180,480];
    case 4
        p1 = [0,25/6,500/3,1260,8400];
        p2 = [1,40,540,3360,8400];
    case 5
        p1 = [0,197/30,210,4620,30240,181440];
        p2 = [1,60,1260,13440,75600,181440];
    case 6
        p1 = [0,49/10,2058/5,8190,137760,813600,4656960];
        p2 = [1,84,2520,40320,378000,1995840,4656960];
    case 7
        p1 = [0, 503/70, 2324/5, 97902/5, 314160, 4527600, 25945920,...
            138378240];
        p2 = [1, 112, 4536, 100800, 1386000, 11975040, 60540480, 138378240];
    case 8
        p1 = [0, 761/140, 54792/70, 29106, 890736, 12612600, 164324160,...
            908107200, 4670265600];
        p2 = [1, 144, 7560, 221760, 4158000, 51891840, 423783360,...
            2075673600, 4670265600];
    case 9
        p1 = [0, 9649/1260, 5841/7, 399366/7, 1633632, 41441400, 540540000,...
            6558552000, 35286451200, 176432256000]; %maybe 389366 or 399...
        p2 = [1, 180, 11880, 4 3520, 10810800, 181621440, 2118916800,...
            16605388800, 79394515200, 176432256000];
    case 10
        p1 = [0, 7381/1260,81191/63, 536679/7, 25767456/7, 90210120,...
            2021187168, 24872047200, 286212326400, 1508495788800,...
            7374868300800];
        p2 = [1, 220, 17820, 823680, 25225200, 544864320, 8475667200,...
            94097203200, 714550636800, 3352212864000, 7374868300800];
    otherwise
        error('incorrect input!');
%         w_n = polyval(p1,z);
end
if length(p1)~=length(p2)
    error('incorrect input');
end
e1=polyval(p1,z)./polyval(p2,z).*z - el - log(z) ;
return